class C 
{
	public static void main(String[] args) 
	{
		// double literals:Any number without single,double quotes but with decimal and decimal 
		// content can be zero also.
		System.out.println(12.34);
		System.out.println(1234.45);
		System.out.println(0.0);
		System.out.println(-234.456);
	}
}
